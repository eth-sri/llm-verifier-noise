    def prepare_for_grpo(self) -> tuple[Dataset, Callable]:

        # NOTE: ideally, this trimmed dataset should have EXACTLY the same prompts as what
        # you get with the same configuration from the offline SFT dataset. this can be
        # currently achieved if you first call the offline SFT created, but this is wasteful.
        # something smarter has to be implemented. also keep in mind that the offline setting
        # might not even make it to the paper, as it is bleeding from a million wounds. so
        # let's see what the future of all this will be.
        trimmed_dataset = self.raw_dataset.remove_columns(
            [c for c in self.raw_dataset.column_names if c not in ["prompt", "answer"]]
        )
        convert_to_message_format = lambda example: {
            "prompt": [
                {"role": "user", "content": example["prompt"] + f"\n{self.PROMPT}"}
            ],
        }
        trimmed_dataset = trimmed_dataset.map(
            convert_to_message_format,
            batched=False,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
        )

        def reward(
            completions: list[list[dict[str, str]]],
            answer: list[str],  # be careful that the name is 'answer' singular
            prompts: list[list[dict[str, str]]],
            **kwargs,
        ) -> list[float]:
            items = [
                {
                    "question": prompt[0]["content"],
                    "answer": ans,
                    "completion_full": completion[0]["content"],
                    "completion_extracted": extract_boxed(completion[0]["content"]),
                }
                for prompt, ans, completion in zip(prompts, answer, completions)
            ]

            def _rule_based_verification(
                completions: list[list[dict[str, str]]], answer: list[str]
            ) -> list[float]:
                # Extract final answers (if any) and build extractability mask
                output_list = [completion[0]["content"] for completion in completions]
                extracted_list = [extract_boxed(content) for content in output_list]

                # Process extracted answers for math verification
                completions_processed = [parse(process_ans(e)) for e in extracted_list]
                answers_processed = [parse(process_ans(ans)) for ans in answer]

                # Ground-truth verification (True/False per completion)
                gt_verification = [
                    verify(completion, ans)
                    for completion, ans in zip(completions_processed, answers_processed)
                ]
                return gt_verification

            def _llm_based_verification(
                completions: list[list[dict[str, str]]],
                answer: list[str],
                prompts: list[list[dict[str, str]]],
            ) -> list[float]:

                def _single_call(client, model_name, prompt, ans, completion):
                    # if completion is not extractable, immediately return false
                    if not extract_boxed(completion[0]["content"]):
                        return "false"
                    if self.verifier_with_gt:
                        message_content = VERIFICATION_PROMPT.format(
                            problem_statement=prompt[0]["content"],
                            solution=ans,
                            proposed_solution=completion[0]["content"],
                        )
                    else:
                        message_content = VERIFICATION_WITHOUT_GT_PROMPT.format(
                            problem_statement=prompt[0]["content"],
                            proposed_solution=completion[0]["content"],
                        )
                    if self.verifier_cot:
                        message_content += WITH_COT
                    else:
                        message_content += WITHOUT_COT
                    request_kwargs = {
                        "model": model_name,
                        "messages": [
                            {
                                "role": "user",
                                "content": message_content,
                            },
                        ],
                        "temperature": self.verifier_temperature,
                        "seed": 42,
                        "extra_body": {
                            "chat_template_kwargs": {
                                "enable_thinking": self.verifier_enable_thinking
                            }
                        },
                    }

                    response = client.chat.completions.create(**request_kwargs)
                    return response

                client = OpenAI(
                    api_key=self.api_key,
                    base_url=f"http://localhost:{self.port}/v1",
                    timeout=300,
                )

                llm_verification = [False] * len(completions)
                verifier_reasoning = [None] * len(completions)
                verifier_cot = [None] * len(completions)
                with ThreadPoolExecutor(max_workers=32) as executor:
                    futures = {
                        executor.submit(
                            _single_call,
                            client,
                            self.verifier,
                            prompt,
                            ans,
                            completion,
                        ): idx
                        for idx, (prompt, ans, completion) in enumerate(
                            zip(prompts, answer, completions)
                        )
                    }
                    for future in tqdm(
                        as_completed(futures), total=len(futures), leave=False
                    ):
                        idx = futures[future]
                        try:
                            response = future.result()
                            # _single_call may return a plain string ("false") for
                            # non-extractable completions. Handle both string and
                            # OpenAI-style response objects robustly.
                            if isinstance(response, str):
                                content = response.strip().lower()
                            else:
                                content = (
                                    response.choices[0].message.content.strip().lower()
                                )
                                if hasattr(response.choices[0].message, "reasoning") and response.choices[0].message.reasoning is not None:
                                    verifier_reasoning[idx] = (
                                        response.choices[0]
                                        .message.reasoning.strip()
                                        .replace("\n", "[LF]")
                                    )
                                else:
                                    match = re.search(
                                        r"<think>(.*?)</think>",
                                        content,
                                        flags=re.DOTALL,
                                    )
                                    if match:
                                        verifier_reasoning[idx] = (
                                            match.group(1).strip().replace("\n", "[LF]")
                                        )

                            match = re.search(
                                r"<think>(.*?)</think>",
                                content,
                                flags=re.DOTALL,
                            )
                            if match:
                                verifier_cot[idx] = match.group(1).strip().replace("\n", "[LF]")
                            # extract final decision
                            output = re.sub(
                                r"<think>.*?</think>",
                                "",
                                content,
                                flags=re.DOTALL,
                            ).strip()
                            decision = extract_tf(output)
                            if idx == 0:
                                print(f"Verification result example:\n{content[:50].replace('\n', ' ')}...{content[-50:].replace('\n', ' ')}")
                            if decision == "true":
                                llm_verification[idx] = True
                            elif decision == "false":
                                llm_verification[idx] = False
                            else:
                                print(
                                    f"Unexpected verification decision for completion {idx}: {decision.replace('\\n', ' ')}"
                                )
                                # best effort - if it contains only one of "true" or "false", reuturn that
                                if "true" in output and "false" not in output:
                                    llm_verification[idx] = True
                                elif "true" not in output and "false" in output:
                                    llm_verification[idx] = False

                                llm_verification[idx] = False  # or handle as needed

                        except Exception as e:

                            def _truncate(s: str, max_len: int = 100) -> str:
                                return re.sub(r"[\r\n]+", " ", str(s))[:max_len]

                            prompt_print = _truncate(prompts[idx][0]["content"])
                            completion_print = _truncate(completions[idx][0]["content"])
                            print(
                                f"Error processing completion {idx}: {e}"
                                f"prompt={prompt_print},"
                                f"answer={answer[idx]},"
                                f"completion(len={len(completions[idx][0]['content'])})={completion_print}..."
                            )
                            llm_verification[idx] = False  # or handle as needed

                reward._verifier_reasoning = verifier_reasoning
                reward._verifier_cot = verifier_cot
                return llm_verification

            if self.verifier is None:
                gt_verification = _rule_based_verification(completions, answer)
                # store for logging
                reward._oracle_rewards = gt_verification
                reward._gt_answers = answer
                reward._extracted_answers = [
                    extract_boxed(completion[0]["content"])
                    for completion in completions
                ]
                return self.mixup.mixup_rewards(
                    gt_verification,
                    items=items,
                )
            else:
                gt_verification = _rule_based_verification(completions, answer)
                llm_verification = _llm_based_verification(completions, answer, prompts)
                # store for logging
                reward._oracle_rewards = gt_verification
                reward._gt_answers = answer
                reward._extracted_answers = [
                    extract_boxed(completion[0]["content"])
                    for completion in completions
                ]
                return self.mixup.mixup_rewards(
                    llm_verification,
                    items=items,
                    oracle_rewards=gt_verification,
                )

        return trimmed_dataset, reward
