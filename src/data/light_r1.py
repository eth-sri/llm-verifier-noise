FilterRules = Literal[
    "none",  # no filtering
    "model_pos",  # keep all with >0 positives of model
    "model_pair",  # keep all with >0 positives and >0 negatives of model
    "pair",  # keep all with >0 positives and >0 negatives
    "double_model_pair",  # keep all with >1 positives and >1 negatives of model
    "double_pair",  # keep all with >1 positives and >1 negatives
]

VERIFICATION_PROMPT = (
    "Below is a problem statement, expected answer, and solution generated by an LLM.\n"
    "Determine whether the generated final answer is correct. Answer 'true' if it is correct, and 'false' otherwise.\n"
    "The reference answer and the answer to be judged only need to be essentially the same, ignoring irrelevant details such as units, symbols, whether or not to approximate, and the form of expression in the answer."
    "The two answers are considered to be consistent if they are equivalently transformable.\n\n"
    "[Problem Statement]\n{problem_statement}\n\n"
    "[Expected Answer]\n{solution}\n\n"
    "[Generated Solution]\n{proposed_solution}\n\n"
    "Now, evaluate the generated final answer.\n"
)

VERIFICATION_WITHOUT_GT_PROMPT = (
    "Below is a problem statement and solution generated by an LLM.\n"
    "Determine whether the generated final answer is correct. Answer 'true' if it is correct, and 'false' otherwise.\n"
    "[Problem Statement]\n{problem_statement}\n\n"
    "[Generated Solution]\n{proposed_solution}\n\n"
    "Now, evaluate the generated final answer.\n"
)

# NOTE I considered different format from the actual QA like <judge></judge>, but the verifier still wraps its decision with boxed{}
# WITH_COT = "Please reason step by step, and put your final answer within <judge></judge>, i.e., <judge>true</judge> or <judge>false</judge>."
WITH_COT = "Please reason step by step, and put your final answer within \\boxed{}, i.e., \\boxed{true} or \\boxed{false}"
WITHOUT_COT = "Do not provide any explanation, and put your answer within \\boxed{}, i.e., \\boxed{true} or \\boxed{false}"


class LightR1(MixedUpDataset):

    PROMPT = "Please reason step by step, and put your final answer within \\boxed{}."

    def __init__(
        self,
        name: str,
        tokenizer: PreTrainedTokenizerBase,
        mixup: Mixup,
        shuffle: bool = True,
        max_length: int = 1024,
        generating_model: str = "deepseek-ai/DeepSeek-R1",
        filter_rule: FilterRules = "model_pos",
        dataset_type: str = "sft",
        # below are for LLM-based verifier
        verifier: str = None,
        port: int = 8000,
        api_key: str = "null",
        verifier_max_tokens: Optional[int] = None,
        verifier_temperature: float = 0.7,
        verifier_with_gt: bool = False,
        verifier_enable_thinking: bool = True,
        verifier_cot: bool = False,
        sort_key: str = None,
        filter_key: str = None,
    ) -> None:
        super().__init__(name, tokenizer, mixup, shuffle, max_length)
        self.filter_rule = filter_rule
        self.generating_model = generating_model
        self.verifier = verifier
        self.port = port
        self.api_key = api_key
        self.verifier_max_tokens = verifier_max_tokens
        self.verifier_temperature = verifier_temperature
        self.verifier_with_gt = verifier_with_gt
        self.verifier_enable_thinking = verifier_enable_thinking
        self.verifier_cot = verifier_cot
        self.sort_key = sort_key
        self.filter_key = filter_key
        self.num_proc = os.cpu_count() // 2
        # NOTE: if we need completely different processing for SFT/DPOData, define separate classes.
        # `dataset_type` indicates in which stage the dataset is used for LighR1 paper, and does not correspond to our training recipe.
        path = "qihoo360/Light-R1-SFTData"
        self.raw_dataset = load_dataset(path, split="train")
        self.raw_dataset = (
            self.raw_dataset.shuffle() if self.shuffle else self.raw_dataset
        )

        if self.sort_key is not None:
            if self.sort_key in self.raw_dataset.column_names:
                self.raw_dataset = self.raw_dataset.sort(self.sort_key, reverse=True)
            else:
                warnings.warn(f"{self.sort_key} not in {self.raw_dataset.column_names}")
        if self.filter_key is not None:
            # remove 0-pass samples
            if self.filter_key in self.raw_dataset.column_names:
                self.raw_dataset = self.raw_dataset.filter(
                    lambda x: x[self.filter_key] > 0,
                    num_proc=self.num_proc,
                    keep_in_memory=True,
                    load_from_cache_file=False,
                )
            else:
                warnings.warn(
                    f"{self.filter_key} not in {self.raw_dataset.column_names}"
                )

        self.raw_dataset = self.raw_dataset.filter(
            self._filter_by_rule,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
            desc="Filtering dataset by rule: " + self.filter_rule,
        )
        self.mixup.set_confusion_matrix(n_samples=len(self.raw_dataset))

    def _filter_by_rule(self, example: dict) -> bool:
        """
        Filter examples based on the specified filtering rule.
        Args:
            example (dict): A dictionary containing the example data with 'positives' and 'negatives' keys.
                           Each positive/negative is expected to have a 'model' field.
        Returns:
            bool: True if the example passes the filter rule, False otherwise.
        Filter Rules:
            - "none": Accept all examples
            - "model_pos": Accept examples that have positives from the generating model
            - "model_pair": Accept examples that have both positives and negatives from the generating model
            - "pair": Accept examples that have both positives and negatives (any model)
            - "double_model_pair": Accept examples with multiple positives and negatives from the generating model
            - "double_pair": Accept examples with multiple positives and negatives from any model
        """
        if self.filter_rule == "none":
            return True
        elif self.filter_rule == "model_pos":
            return any(
                p["model"] == self.generating_model for p in example["positives"]
            )
        elif self.filter_rule == "model_pair":
            return any(
                p["model"] == self.generating_model for p in example["positives"]
            ) and any(n["model"] == self.generating_model for n in example["negatives"])
        elif self.filter_rule == "pair":
            return len(example["positives"]) > 0 and len(example["negatives"]) > 0
        elif self.filter_rule == "double_model_pair":
            return (
                sum(p["model"] == self.generating_model for p in example["positives"])
                > 1
                and sum(
                    n["model"] == self.generating_model for n in example["negatives"]
                )
                > 1
            )
        elif self.filter_rule == "double_pair":
            return len(example["positives"]) > 1 and len(example["negatives"]) > 1

    def _to_messages(self, example: dict) -> list[dict[str, str]]:
        return {
            "proc_messages": [
                {
                    "role": "user",
                    "content": example["conversations"][0]["value"]
                    + f"\n{self.PROMPT}",
                },
                {"role": "assistant", "content": example["conversations"][1]["value"]},
            ]
        }

    def prepare_for_sft(self) -> Dataset:
        """
        Takes the dataset and returns it already tokenized, ready for SFT training. It mixes up
        the labels in order to achieve the mixup specified in the Mixup object.
        """

        n_pos = self.mixup.TP
        n_neg = self.mixup.FP

        if n_pos > 0:
            pos_set = self.raw_dataset.select(range(n_pos)).remove_columns(
                ["negatives"]
            )
        else:
            pos_set = self.raw_dataset.select([]).remove_columns(["negatives"])
        if n_neg > 0:
            neg_set = self.raw_dataset.select(
                range(n_pos, n_pos + n_neg)
            ).remove_columns(["positives"])
        else:
            neg_set = self.raw_dataset.select([]).remove_columns(["positives"])

        def tokenize_function(example: dict, negative: bool = False) -> dict:
            marker = "negatives" if negative else "positives"
            if "model" in self.filter_rule:
                options = [
                    option
                    for option in example[marker]
                    if option["model"] == self.generating_model
                ]
            else:
                options = example[marker]
            if len(options) == 0:
                raise ValueError(
                    f"""
                    No {marker} completions found for this example. Perhaps you set the filtering
                    rule wrong. If negatives are needed, make sure that you set either pair, or
                    double_pair rules.
                    """
                )
            idx_option = np.random.randint(0, len(options))
            prompt = example["prompt"] + f"\n{self.PROMPT}"
            target_reasoning, target_final_ans = (
                options[idx_option]["reasoning"],
                options[idx_option]["final_answer"],
            )
            target = f"<think>\n{target_reasoning}\n</think>\n{target_final_ans}"
            messages = [
                {"role": "user", "content": prompt},
                {"role": "assistant", "content": target},
            ]
            return self.tokenizer.apply_chat_template(
                messages,
                tokenize=True,
                return_dict=True,
                max_length=self.max_length,
                padding="max_length",
            )

        pos_set = pos_set.map(
            tokenize_function,
            fn_kwargs={"negative": False},
            batched=False,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
        )
        pos_set = pos_set.remove_columns(
            [
                c
                for c in pos_set.column_names
                if c not in ["input_ids", "attention_mask"]
            ]
        )

        neg_set = neg_set.map(
            tokenize_function,
            fn_kwargs={"negative": True},
            batched=False,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
        )
        neg_set = neg_set.remove_columns(
            [
                c
                for c in neg_set.column_names
                if c not in ["input_ids", "attention_mask"]
            ]
        )

        mixed_dataset = concatenate_datasets([pos_set, neg_set]).shuffle()
        del pos_set, neg_set
        mixed_dataset = mixed_dataset.map(
            lambda x: {"labels": x["input_ids"]},
            batched=False,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
        )

        mixed_dataset = mixed_dataset.filter(
            lambda x: len(x["input_ids"]) <= self.max_length,
            num_proc=self.num_proc,
            keep_in_memory=True,
            load_from_cache_file=False,
        )
        return mixed_dataset

